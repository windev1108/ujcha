export function encodeTCVN3(text: string): Buffer {
    const bytes: number[] = []
    for (const char of text) {
        const cp = char.codePointAt(0)!
        if (cp <= 0x7f) {
            bytes.push(cp)
            continue
        }
        const b = TCVN3_MAP[cp]
        if (b !== undefined) {
            bytes.push(b)
            continue
        }
        // Fallback: bỏ dấu về ASCII
        const stripped = char
            .normalize('NFD')
            .replace(/[\u0300-\u036f]/g, '')
            .replace(/[đĐ]/g, c => c === 'đ' ? 'd' : 'D')
        const fb = stripped.codePointAt(0)
        bytes.push(fb !== undefined && fb <= 0x7f ? fb : 0x3f)
    }
    return Buffer.from(bytes)
}

export function tcvn3ln(text: string): Buffer {
    return Buffer.concat([encodeTCVN3(text), Buffer.from([0x0a])])
}